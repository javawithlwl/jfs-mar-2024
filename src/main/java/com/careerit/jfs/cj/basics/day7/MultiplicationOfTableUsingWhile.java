package com.careerit.jfs.cj.basics.day7;

public class MultiplicationOfTableUsingWhile {


    public static void main(String[] args) {
        int num  = 19;
        int i = 1;
        while(i <= 10){
            System.out.println(num + " * " + i + " = " + (num * i));
            i++;
        }

    }
}
